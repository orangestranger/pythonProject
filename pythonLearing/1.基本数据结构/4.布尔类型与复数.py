# bool类型表示真、假
# complex复数
真 = True
假 = False

print(type(真))
print(type(假))

print(int(真))
print(int(假))


print(bool(1))  # 只要是非0的数都是真
print(bool(0))

print(bool('abc'))
print(bool(''))

print(bool([1, 2, 3]))
print(bool([]))

print(bool({1, 1, 1,}))
print(bool({}))

print(bool(None))  # False

# 复数
print(36j)

